def _q(
    question_id: str,
    question_code: str,
    category: str,
    question_text: str,
    display_order: int,
    default_value: str = "UNKNOWN",
    help_text: str = "",
    beginner_guidance: str = "",
) -> dict:
    return {
        "question_id": question_id,
        "question_code": question_code,
        "category": category,
        "question_text": question_text,
        "question_type": "YESNO",
        "default_value": default_value,
        "required_flag": 1,
        "display_order": display_order,
        "help_text": help_text,
        "beginner_guidance": beginner_guidance,
        "is_active": 1,
    }


